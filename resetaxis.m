function resetaxis(handles)
axes(handles.axes1);
cla reset;
grid on;
axis on;
hold on;
axis([0 100 0 100]);

axes(handles.axes2);
cla reset;
grid on;
axis on;
hold on;
axis([0 100 0 100]);

axes(handles.axes3);
cla reset;
grid on;
axis on;
hold on;
axis([0 100 0 100]);

axes(handles.axes4);
cla reset;
grid on;
axis on;
hold on;
axis([0 100 0 100]);

end

